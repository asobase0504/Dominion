//-----------------------------------------
// 弾を発射する処理
//-----------------------------------------
COperate::SHOT_TYPE CAIOperate::BulletShot()
{
	static int count = 0;
	count++;

	if (count % 30 == 0)
	{
		count = 0;
		return UP_SHOT;
	}
	return NONE_SHOT;
}
