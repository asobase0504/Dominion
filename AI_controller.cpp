//-----------------------------------------
// 弾を発射する処理
//-----------------------------------------
CController::SHOT_TYPE CAIController::BulletShot()
{
	static int count = 0;
	count++;

	if (count % 30 == 0)
	{
		count = 0;
		return UP_SHOT;
	}
	return NONE_SHOT;
}
